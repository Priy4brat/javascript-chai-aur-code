class User {
    constructor(username){
        this.username = username
    }

    logMe(){
        console.log(`USERNAME is ${this.username}`);
    }
}

class Teacher extends User{  // extends keyword basicvaslly gives the properties of user to the the teacher
    constructor(username, email, password){
        super(username) // super keyaword basically allows to acesss the properties if parents in child 
        this.email = email
        this.password = password
    }

    addCourse(){
        console.log(`A new course was added by ${this.username}`);
    }
}

